// if
const name = "Alberto";

if (name == "Carlos") {
  console.log(`Tu eres ${name}`);
}

// else
if (name == "Carlos") {
  console.log(`Tu eres ${name}`);
} else {
  console.log(`En realidad tu eres ${name}`);
}

// else if
if (name == "Carlos") {
  console.log(`Tu eres ${name}`);
} else if (name == "Juan") {
  console.log(`En realidad tu eres ${name}`);
} else {
  console.log(`Asi que al final tu eres ${name}`);
}

